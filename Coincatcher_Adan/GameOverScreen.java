import greenfoot.*;

/**
 * The GameOverScreen class represents the game over screen.
 * 
 * @author (Adan Mohamed)
 * @version (Version1.0)
 */
public class GameOverScreen extends Actor
{
    /**
     * Constructor for the GameOverScreen class.
     */
    public GameOverScreen()
    {
        // Set the image to a new GreenfootImage with text "Game Over", font size 48, white font color, and black background
        setImage(new GreenfootImage("Game Over", 48, Color.WHITE, Color.BLACK));
    }
}
