public class Point  
{
    private int x;
    private int y;
    
    public Point(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
    
    //Return x coordinate
    public int getX()
    {
        return x;
    }

    //Return y coordinate
    public int getY()
    {
        return y;
    }
}
